'use client';

export const dynamic = 'force-dynamic';

import { useEffect, useState } from 'react';
import { useRouter } from 'next/navigation';
import Link from 'next/link';
import { Sparkles, Download, MessageSquare, CircleCheck as CheckCircle, CircleAlert as AlertCircle, Circle as XCircle, LogOut } from 'lucide-react';
import { createClient } from '@/lib/supabase/client';

type Recommendation = {
  id: string;
  best_fit: {
    field: string;
    degrees: string[];
    jobs: string[];
    avg_salary: string;
    why: string;
  };
  secondary: {
    field: string;
    degrees: string[];
    jobs: string[];
    avg_salary: string;
    why: string;
  };
  avoid: {
    field: string;
    why: string;
  };
  reasoning: {
    interests: string;
    skills: string;
    lifestyle: string;
    finances: string;
    market: string;
  };
};

export default function ResultsPage() {
  const [user, setUser] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const [recommendation, setRecommendation] = useState<Recommendation | null>(null);
  const [showMentorForm, setShowMentorForm] = useState(false);
  const [mentorForm, setMentorForm] = useState({
    name: '',
    email: '',
    chosen_field: '',
    message: '',
  });
  const [submitting, setSubmitting] = useState(false);
  const [submitted, setSubmitted] = useState(false);
  const router = useRouter();
  const supabase = createClient();

  useEffect(() => {
    const loadRecommendation = async () => {
      const { data: { user } } = await supabase.auth.getUser();
      if (!user) {
        router.push('/auth');
        return;
      }
      setUser(user);

      const { data: recommendations } = await supabase
        .from('recommendations')
        .select('*')
        .eq('user_id', user.id)
        .order('created_at', { ascending: false })
        .limit(1);

      if (recommendations && recommendations.length > 0) {
        setRecommendation(recommendations[0]);
      } else {
        router.push('/start');
      }

      setLoading(false);
    };

    loadRecommendation();
  }, [router, supabase]);

  const handleSignOut = async () => {
    await supabase.auth.signOut();
    router.push('/');
  };

  const downloadReport = () => {
    if (!recommendation) return;

    const reportContent = `
WHAT NEXT - CAREER CLARITY REPORT
================================

Student: ${user?.user_metadata?.name || user?.email}
Date: ${new Date().toLocaleDateString()}

YOUR PROFILE
------------
Interests: ${recommendation.reasoning.interests}
Skills: ${recommendation.reasoning.skills}
Lifestyle: ${recommendation.reasoning.lifestyle}
Finances: ${recommendation.reasoning.finances}
Market Insights: ${recommendation.reasoning.market}

BEST FIT RECOMMENDATION
----------------------
Field: ${recommendation.best_fit.field}
Degrees: ${recommendation.best_fit.degrees.join(', ')}
Example Jobs: ${recommendation.best_fit.jobs.join(', ')}
Average Salary in India: ${recommendation.best_fit.avg_salary}

Why this fits you:
${recommendation.best_fit.why}

SECONDARY OPTION
----------------
Field: ${recommendation.secondary.field}
Degrees: ${recommendation.secondary.degrees.join(', ')}
Example Jobs: ${recommendation.secondary.jobs.join(', ')}
Average Salary in India: ${recommendation.secondary.avg_salary}

Why worth considering:
${recommendation.secondary.why}

PATH TO AVOID
-------------
Field: ${recommendation.avoid.field}
Reasoning: ${recommendation.avoid.why}

---
Generated by What Next - AI Career Guidance
    `.trim();

    const blob = new Blob([reportContent], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'career-clarity-report.txt';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  const submitMentorRequest = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!user) return;

    setSubmitting(true);

    try {
      const { error } = await supabase.from('mentor_requests').insert({
        user_id: user.id,
        name: mentorForm.name,
        email: mentorForm.email,
        chosen_field: mentorForm.chosen_field,
        message: mentorForm.message,
      });

      if (error) throw error;

      setSubmitted(true);
      setTimeout(() => {
        setShowMentorForm(false);
        setSubmitted(false);
        setMentorForm({ name: '', email: '', chosen_field: '', message: '' });
      }, 2000);
    } catch (error) {
      console.error('Error submitting mentor request:', error);
    } finally {
      setSubmitting(false);
    }
  };

  if (loading) {
    return (
      <div className="min-h-screen bg-white flex items-center justify-center">
        <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-[#6C63FF]"></div>
      </div>
    );
  }

  if (!recommendation) {
    return (
      <div className="min-h-screen bg-white flex items-center justify-center">
        <div className="text-center">
          <p className="text-gray-600">No recommendation found.</p>
          <Link href="/start" className="text-[#6C63FF] hover:underline mt-4 inline-block">
            Start a new conversation
          </Link>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-white flex flex-col">
      <nav className="px-6 py-4 border-b border-gray-200">
        <div className="max-w-5xl mx-auto flex items-center justify-between">
          <Link href="/" className="flex items-center gap-2">
            <Sparkles className="w-6 h-6 text-[#6C63FF]" />
            <span className="text-xl font-bold text-[#1A1A2E]">What Next</span>
          </Link>
          <button
            onClick={handleSignOut}
            className="flex items-center gap-2 text-gray-600 hover:text-[#1A1A2E] transition-colors"
          >
            <LogOut className="w-4 h-4" />
            <span className="text-sm">Sign out</span>
          </button>
        </div>
      </nav>

      <main className="flex-1 px-6 py-12">
        <div className="max-w-5xl mx-auto">
          <div className="text-center mb-12">
            <h1 className="text-3xl md:text-4xl font-bold text-[#1A1A2E] mb-4">
              Here's what we think, {user?.user_metadata?.name || 'there'}
            </h1>
            <p className="text-lg text-gray-600">
              Based on our conversation, here are your personalized career recommendations
            </p>
          </div>

          <div className="space-y-8">
            <div className="bg-green-50 border-2 border-green-500 rounded-lg p-8">
              <div className="flex items-start gap-3 mb-4">
                <CheckCircle className="w-6 h-6 text-green-600 flex-shrink-0 mt-1" />
                <div>
                  <h2 className="text-2xl font-bold text-[#1A1A2E] mb-2">Best fit for you</h2>
                  <h3 className="text-xl font-semibold text-green-700 mb-4">
                    {recommendation.best_fit.field}
                  </h3>
                </div>
              </div>

              <div className="grid md:grid-cols-3 gap-4 mb-4">
                <div>
                  <p className="text-sm font-semibold text-gray-700 mb-1">Example Degrees</p>
                  <ul className="text-sm text-gray-600 space-y-1">
                    {recommendation.best_fit.degrees.map((degree, i) => (
                      <li key={i}>• {degree}</li>
                    ))}
                  </ul>
                </div>
                <div>
                  <p className="text-sm font-semibold text-gray-700 mb-1">Example Jobs</p>
                  <ul className="text-sm text-gray-600 space-y-1">
                    {recommendation.best_fit.jobs.map((job, i) => (
                      <li key={i}>• {job}</li>
                    ))}
                  </ul>
                </div>
                <div>
                  <p className="text-sm font-semibold text-gray-700 mb-1">Avg. Salary in India</p>
                  <p className="text-sm text-gray-600">{recommendation.best_fit.avg_salary}</p>
                </div>
              </div>

              <div className="bg-white rounded-lg p-4">
                <p className="text-sm font-semibold text-gray-700 mb-2">Why this fits you</p>
                <p className="text-gray-600">{recommendation.best_fit.why}</p>
              </div>
            </div>

            <div className="bg-blue-50 border-2 border-blue-500 rounded-lg p-8">
              <div className="flex items-start gap-3 mb-4">
                <AlertCircle className="w-6 h-6 text-blue-600 flex-shrink-0 mt-1" />
                <div>
                  <h2 className="text-2xl font-bold text-[#1A1A2E] mb-2">Also worth considering</h2>
                  <h3 className="text-xl font-semibold text-blue-700 mb-4">
                    {recommendation.secondary.field}
                  </h3>
                </div>
              </div>

              <div className="grid md:grid-cols-3 gap-4 mb-4">
                <div>
                  <p className="text-sm font-semibold text-gray-700 mb-1">Example Degrees</p>
                  <ul className="text-sm text-gray-600 space-y-1">
                    {recommendation.secondary.degrees.map((degree, i) => (
                      <li key={i}>• {degree}</li>
                    ))}
                  </ul>
                </div>
                <div>
                  <p className="text-sm font-semibold text-gray-700 mb-1">Example Jobs</p>
                  <ul className="text-sm text-gray-600 space-y-1">
                    {recommendation.secondary.jobs.map((job, i) => (
                      <li key={i}>• {job}</li>
                    ))}
                  </ul>
                </div>
                <div>
                  <p className="text-sm font-semibold text-gray-700 mb-1">Avg. Salary in India</p>
                  <p className="text-sm text-gray-600">{recommendation.secondary.avg_salary}</p>
                </div>
              </div>

              <div className="bg-white rounded-lg p-4">
                <p className="text-sm font-semibold text-gray-700 mb-2">Why consider this</p>
                <p className="text-gray-600">{recommendation.secondary.why}</p>
              </div>
            </div>

            <div className="bg-red-50 border-2 border-red-500 rounded-lg p-8">
              <div className="flex items-start gap-3 mb-4">
                <XCircle className="w-6 h-6 text-red-600 flex-shrink-0 mt-1" />
                <div>
                  <h2 className="text-2xl font-bold text-[#1A1A2E] mb-2">Think carefully about</h2>
                  <h3 className="text-xl font-semibold text-red-700 mb-4">
                    {recommendation.avoid.field}
                  </h3>
                </div>
              </div>

              <div className="bg-white rounded-lg p-4">
                <p className="text-sm font-semibold text-gray-700 mb-2">Why to avoid</p>
                <p className="text-gray-600">{recommendation.avoid.why}</p>
              </div>
            </div>

            <div className="flex flex-col sm:flex-row gap-4 justify-center pt-8">
              <button
                onClick={downloadReport}
                className="flex items-center justify-center gap-2 bg-[#6C63FF] text-white px-6 py-3 rounded-lg font-medium hover:bg-[#5B52E8] transition-colors"
              >
                <Download className="w-5 h-5" />
                Download your clarity report
              </button>
              <button
                onClick={() => setShowMentorForm(true)}
                className="flex items-center justify-center gap-2 bg-white border-2 border-[#6C63FF] text-[#6C63FF] px-6 py-3 rounded-lg font-medium hover:bg-[#6C63FF] hover:text-white transition-colors"
              >
                <MessageSquare className="w-5 h-5" />
                Talk to a mentor
              </button>
            </div>
          </div>
        </div>
      </main>

      {showMentorForm && (
        <div className="fixed inset-0 bg-black/50 flex items-center justify-center p-6 z-50">
          <div className="bg-white rounded-lg max-w-md w-full p-8">
            <h2 className="text-2xl font-bold text-[#1A1A2E] mb-4">Talk to a mentor</h2>

            {submitted ? (
              <div className="text-center py-8">
                <CheckCircle className="w-16 h-16 text-green-500 mx-auto mb-4" />
                <p className="text-lg font-semibold text-[#1A1A2E]">Request submitted!</p>
                <p className="text-gray-600 mt-2">We'll be in touch soon.</p>
              </div>
            ) : (
              <form onSubmit={submitMentorRequest} className="space-y-4">
                <div>
                  <label className="block text-sm font-medium text-[#1A1A2E] mb-2">
                    Your name
                  </label>
                  <input
                    type="text"
                    value={mentorForm.name}
                    onChange={(e) => setMentorForm({ ...mentorForm, name: e.target.value })}
                    className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-[#6C63FF]"
                    required
                  />
                </div>

                <div>
                  <label className="block text-sm font-medium text-[#1A1A2E] mb-2">
                    Email
                  </label>
                  <input
                    type="email"
                    value={mentorForm.email}
                    onChange={(e) => setMentorForm({ ...mentorForm, email: e.target.value })}
                    className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-[#6C63FF]"
                    required
                  />
                </div>

                <div>
                  <label className="block text-sm font-medium text-[#1A1A2E] mb-2">
                    Field you're interested in
                  </label>
                  <input
                    type="text"
                    value={mentorForm.chosen_field}
                    onChange={(e) => setMentorForm({ ...mentorForm, chosen_field: e.target.value })}
                    className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-[#6C63FF]"
                    required
                  />
                </div>

                <div>
                  <label className="block text-sm font-medium text-[#1A1A2E] mb-2">
                    Message (optional)
                  </label>
                  <textarea
                    value={mentorForm.message}
                    onChange={(e) => setMentorForm({ ...mentorForm, message: e.target.value })}
                    className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-[#6C63FF] min-h-[100px]"
                    placeholder="Any specific questions or concerns..."
                  />
                </div>

                <div className="flex gap-3">
                  <button
                    type="button"
                    onClick={() => setShowMentorForm(false)}
                    className="flex-1 px-4 py-2 border border-gray-300 rounded-lg hover:bg-gray-50 transition-colors"
                  >
                    Cancel
                  </button>
                  <button
                    type="submit"
                    disabled={submitting}
                    className="flex-1 bg-[#6C63FF] text-white px-4 py-2 rounded-lg hover:bg-[#5B52E8] transition-colors disabled:opacity-50"
                  >
                    {submitting ? 'Submitting...' : 'Submit'}
                  </button>
                </div>
              </form>
            )}
          </div>
        </div>
      )}
    </div>
  );
}
